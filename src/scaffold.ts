function pyFileTool(name: string): Record<string, string> {
  const pkg = sanitizeName(name).replace(/-/g, '_');
  const files = pyReadOnlyApi(name);
  files['src/server.py'] = `#!/usr/bin/env python3
import asyncio
import os
from pathlib import Path
from mcp.server import Server
from mcp.server.stdio import stdio_server
from mcp.types import TextContent

DATA_DIR = Path(os.environ.get("DATA_DIR", "./data"))
DATA_DIR.mkdir(exist_ok=True)

server = Server("${pkg}")

@server.list_tools()
async def list_tools() -> list:
    return [
        {
            "name": "read_file",
            "description": "Read a file from the data directory",
            "inputSchema": {
                "type": "object",
                "properties": {
                    "path": {"type": "string", "description": "Relative file path"},
                },
                "required": ["path"],
            },
        },
        {
            "name": "list_files",
            "description": "List files in the data directory",
            "inputSchema": {"type": "object", "properties": {}},
        },
        {
            "name": "write_file",
            "description": "Write a file to the data directory",
            "inputSchema": {
                "type": "object",
                "properties": {
                    "path": {"type": "string"},
                    "content": {"type": "string"},
                },
                "required": ["path", "content"],
            },
        },
    ]

@server.call_tool()
async def call_tool(name: str, arguments: dict) -> list:
    file_path = DATA_DIR / arguments.get("path", "")
    if name == "read_file":
        if not file_path.exists():
            raise ValueError(f"File not found: {arguments.get('path')}")
        return [TextContent(type="text", text=file_path.read_text())]
    elif name == "list_files":
        return [TextContent(type="text", text="\\n".join(f.name for f in DATA_DIR.iterdir()))]
    elif name == "write_file":
        file_path.write_text(arguments.get("content", ""))
        return [TextContent(type="text", text=f"Wrote {arguments.get('path')}")]
    raise ValueError(f"Unknown tool: {name}")

async def main():
    async with stdio_server() as (read_stream, write_stream):
        await server.run(read_stream, write_stream, server.create_initialization_options())

if __name__ == "__main__":
    asyncio.run(main())
`;
  return files;
}

function pyDbTool(name: string): Record<string, string> {
  const pkg = sanitizeName(name).replace(/-/g, '_');
  const files = pyReadOnlyApi(name);
  files['pyproject.toml'] = `[build-system]
requires = ["hatchling"]
build-backend = "hatchling.build"

[project]
name = "${pkg}"
version = "0.1.0"
description = "MCP server generated by mcp-forge"
requires-python = ">=3.10"
dependencies = ["mcp>=1.0.0"]

[project.scripts]
${pkg} = "${pkg}.server:main"
`;

  files['src/server.py'] = `#!/usr/bin/env python3
import asyncio
import os
import sqlite3
from mcp.server import Server
from mcp.server.stdio import stdio_server
from mcp.types import TextContent

DB_PATH = os.environ.get("DB_PATH", "./data.db")

server = Server("${pkg}")

@server.list_tools()
async def list_tools() -> list:
    return [
        {
            "name": "query",
            "description": "Run a SELECT query against the database",
            "inputSchema": {
                "type": "object",
                "properties": {
                    "sql": {"type": "string", "description": "SQL SELECT statement"},
                },
                "required": ["sql"],
            },
        },
    ]

@server.call_tool()
async def call_tool(name: str, arguments: dict) -> list:
    if name == "query":
        sql = arguments.get("sql", "").strip()
        if not sql.lower().startswith("select"):
            raise ValueError("Only SELECT queries are allowed")
        conn = sqlite3.connect(DB_PATH)
        conn.row_factory = sqlite3.Row
        rows = conn.execute(sql).fetchall()
        conn.close()
        return [TextContent(type="text", text=str([dict(r) for r in rows]))]
    raise ValueError(f"Unknown tool: {name}")

async def main():
    async with stdio_server() as (read_stream, write_stream):
        await server.run(read_stream, write_stream, server.create_initialization_options())

if __name__ == "__main__":
    asyncio.run(main())
`;
  return files;
}

const TS_TEMPLATES: Record<Template, (name: string) => Record<string, string>> = {
  'read-only-api': tsReadOnlyApi,
  'file-tool': tsFileTool,
  'db-tool': tsDbTool,
};

const PY_TEMPLATES: Record<Template, (name: string) => Record<string, string>> = {
  'read-only-api': pyReadOnlyApi,
  'file-tool': pyFileTool,
  'db-tool': pyDbTool,
};

export function scaffold(opts: ScaffoldOptions): string {
  const targetDir = join(opts.outDir, opts.name);
  if (existsSync(targetDir)) {
    throw new Error(`Directory already exists: ${targetDir}`);
  }

  const templates = opts.lang === 'typescript' ? TS_TEMPLATES : PY_TEMPLATES;
  const files = templates[opts.template](opts.name);

  for (const [filePath, content] of Object.entries(files)) {
    const fullPath = join(targetDir, filePath);
    mkdirSync(join(fullPath, '..'), { recursive: true });
    writeFileSync(fullPath, content);
  }

  return targetDir;
}

export function listTemplates(): { id: Template; title: string; description: string }[] {
  return Object.entries(TEMPLATES).map(([id, t]) => ({ id: id as Template, ...t }));
}
