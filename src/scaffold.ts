/**
 * Scaffold logic for mcp-forge
 * ============================
 * Generates TS or Python MCP server projects from templates.
 */

import { mkdirSync, writeFileSync, existsSync } from 'fs';
import { join } from 'path';

export type Language = 'typescript' | 'python';
export type Template = 'read-only-api' | 'file-tool' | 'db-tool';

export interface ScaffoldOptions {
  name: string;
  lang: Language;
  template: Template;
  outDir: string;
}

const TEMPLATES: Record<Template, { title: string; description: string }> = {
  'read-only-api': {
    title: 'Read-Only API Wrapper',
    description: 'Wraps a read-only REST API as MCP tools.',
  },
  'file-tool': {
    title: 'File-Based Tool',
    description: 'Reads, writes, and searches files in a directory.',
  },
  'db-tool': {
    title: 'Database-Backed Tool',
    description: 'Queries a SQLite database via MCP tools.',
  },
};

function sanitizeName(name: string): string {
  return name.toLowerCase().replace(/[^a-z0-9_-]/g, '-').replace(/^-+|-+$/g, '');
}

