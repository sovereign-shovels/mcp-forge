function tsFileTool(name: string): Record<string, string> {
  const pkg = sanitizeName(name);
  const files = tsReadOnlyApi(name);
  files['src/index.ts'] = `#!/usr/bin/env node
import { Server } from "@modelcontextprotocol/sdk/server/index.js";
import { StdioServerTransport } from "@modelcontextprotocol/sdk/server/stdio.js";
import { ListToolsRequestSchema, CallToolRequestSchema } from "@modelcontextprotocol/sdk/types.js";
import { readFileSync, readdirSync, writeFileSync, existsSync } from "fs";
import { join } from "path";

const DATA_DIR = process.env.DATA_DIR || "./data";

const server = new Server(
  { name: "${pkg}", version: "0.1.0" },
  { capabilities: { tools: {} } }
);

server.setRequestHandler(ListToolsRequestSchema, async () => ({
  tools: [
    {
      name: "read_file",
      description: "Read a file from the data directory",
      inputSchema: {
        type: "object",
        properties: {
          path: { type: "string", description: "Relative file path" },
        },
        required: ["path"],
      },
    },
    {
      name: "list_files",
      description: "List files in the data directory",
      inputSchema: {
        type: "object",
        properties: {},
      },
    },
    {
      name: "write_file",
      description: "Write a file to the data directory",
      inputSchema: {
        type: "object",
        properties: {
          path: { type: "string" },
          content: { type: "string" },
        },
        required: ["path", "content"],
      },
    },
  ],
}));

server.setRequestHandler(CallToolRequestSchema, async (request) => {
  const args = request.params.arguments as Record<string, string>;
  const filePath = join(DATA_DIR, args.path || "");

  switch (request.params.name) {
    case "read_file":
      if (!existsSync(filePath)) throw new Error(\`File not found: \${args.path}\`);
      return { content: [{ type: "text", text: readFileSync(filePath, "utf-8") }] };
    case "list_files":
      return { content: [{ type: "text", text: readdirSync(DATA_DIR).join("\\n") }] };
    case "write_file":
      writeFileSync(filePath, args.content);
      return { content: [{ type: "text", text: \`Wrote \${args.path}\` }] };
    default:
      throw new Error(\`Unknown tool: \${request.params.name}\`);
  }
});

async function main() {
  const transport = new StdioServerTransport();
  await server.connect(transport);
}

main().catch(console.error);
`;
  return files;
}

function tsDbTool(name: string): Record<string, string> {
  const pkg = sanitizeName(name);
  const files = tsReadOnlyApi(name);
  files['package.json'] = JSON.stringify({
    name: pkg,
    version: '0.1.0',
    description: `MCP server: ${TEMPLATES['db-tool'].description}`,
    type: 'module',
    bin: { [pkg]: 'dist/index.js' },
    scripts: {
      build: 'tsc',
      start: 'node dist/index.js',
      test: 'node --test dist/**/*.test.js',
    },
    dependencies: {
      '@modelcontextprotocol/sdk': '^1.0.0',
      'better-sqlite3': '^9.0.0',
    },
    devDependencies: {
      '@types/better-sqlite3': '^7.6.0',
      '@types/node': '^20.0.0',
      typescript: '^5.4.0',
    },
  }, null, 2);

  files['src/index.ts'] = `#!/usr/bin/env node
import { Server } from "@modelcontextprotocol/sdk/server/index.js";
import { StdioServerTransport } from "@modelcontextprotocol/sdk/server/stdio.js";
import { ListToolsRequestSchema, CallToolRequestSchema } from "@modelcontextprotocol/sdk/types.js";
import Database from "better-sqlite3";

const DB_PATH = process.env.DB_PATH || "./data.db";
const db = new Database(DB_PATH);

const server = new Server(
  { name: "${pkg}", version: "0.1.0" },
  { capabilities: { tools: {} } }
);

server.setRequestHandler(ListToolsRequestSchema, async () => ({
  tools: [
    {
      name: "query",
      description: "Run a SELECT query against the database",
      inputSchema: {
        type: "object",
        properties: {
          sql: { type: "string", description: "SQL SELECT statement" },
        },
        required: ["sql"],
      },
    },
  ],
}));

server.setRequestHandler(CallToolRequestSchema, async (request) => {
  if (request.params.name === "query") {
    const { sql } = request.params.arguments as { sql: string };
    if (!sql.trim().toLowerCase().startsWith("select")) {
      throw new Error("Only SELECT queries are allowed");
    }
    const rows = db.prepare(sql).all();
    return { content: [{ type: "text", text: JSON.stringify(rows, null, 2) }] };
  }
  throw new Error(\`Unknown tool: \${request.params.name}\`);
});

async function main() {
  const transport = new StdioServerTransport();
  await server.connect(transport);
}

main().catch(console.error);
`;
  return files;
}

